//Ejercicios de switch y case e break

let frutas = "Manzana"

switch(frutas) {
    case "Uva":
        console.log("Las uvas cuestan 20 pesos")
    break

    case  "Manzana":
        console.log("Las manzanas cuestan 30 pesos")
    break

    case "Papaya":
        console.log("Las papayas cuestan 30 pesos")
    break

    case "Naranjas":
        console.log("las naranjas cuestan 15 pesos")
    break

    default:
    console.log("No se encontro  la fruta")
}